menu_pilihan = input(str('silahkan pilih menu [1-3] :'))

if menu_pilihan == '1':
    print('anda memilih menu 1')

elif menu_pilihan == '2':
    print('anda memilih menu 2')

elif menu_pilihan == '3':
    print('anda memilih menu 3')

else:
    print('menu tidak ada dalam daftar')


