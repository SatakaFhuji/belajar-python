#operasi aritmatika
a = 11
b = 3

#operasi penjumlahan/tambah +
hasil = a + b
print(a,'+',b,'=',hasil)

#operasi pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)

#operasi perkalian *
hasil = a * b
print(a,'*',b,'=',hasil)

#operasi pembagian /
hasil = a / b
print(a,'/',b,'=',hasil)

#operasi eksponen (pangkat) **
hasil = a ** b
print(a,'**',b,'=',hasil)

#operasi modulus (sisa pembagian) %
hasil = a % b
print(a,'%',b,'=',hasil)

#operasi floor division (//)
hasil = a // b
print(a,'//',b,'=',hasil)

'''
    *prioritas operasi, operational precedence
    *urutan melakukan prioritas operasi
    1. di dalam kurung terlebih dahulu ()
    2. selanjutnya exponen/pangkat **
    3. selanjutnya perkalian, pembagian, sisa bagi, floor devision. *, /, %, //
    4. terakhir pertambahan dan pengurangan +, -
'''