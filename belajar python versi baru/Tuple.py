# belajar Tuple
# tuple adalah data yg imutable(tidak bisa dirubah isinya)
# seperti list akan tetapi Tuple ini menggunakan kurung tutup biasa ()

pelanggan = ('adli', 'purnomo', 'andi')

# untuk mengakses sama seperti list, menggunakan index
print(pelanggan[0])
print(pelanggan[1])
print(pelanggan[2])

# juga bisa menggunakan for loop
for i in pelanggan:
    print(i)
